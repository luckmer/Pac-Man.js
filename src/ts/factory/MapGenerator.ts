class MapGenerator {
  public table: HTMLDivElement[];

  constructor() {
    this.table = [];
  }

  set() {
    for (let i = 0; i < grid.length; i++) {
      const div = document.createElement("div");
      div.id = i.toString();
      Map?.appendChild(div);
      this.table.push(div);
      if (grid[i] === 1) {
        this.table[i].className = "wall";
      }
      if (grid[i] === 2) {
        this.table[i].className = "EmptySpace";
      }
      if (grid[i] === 0) {
        this.table[i].className = "space";
      }
      if (grid[i] === 3) {
        this.table[i].className = "apple";
      }
      if (grid[i] === 4) {
        this.table[i].className = "EmptySpace";
      }
    }
  }

  get() {
    const array = this.table;
    return array;
  }
}

const mapGenerator = new MapGenerator();
mapGenerator.set();

export default mapGenerator;
